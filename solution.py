"""
Язык: строка символов из алфавита {+, -, a, b},
начинающаяся с '+' и заканчивающаяся на '+',
между которыми расположена последовательность символов a,b
(может быть пустой), в которой любая пара одинаковых символов
разделяется символом '-'.

Примеры допустимых цепочек: ++, +a+, +b+, +ab+, +ba+,
+a-a+, +b-b+, +abab+, +a-a-ab-b-bab-b+

Регулярное выражение (РВ), задающее язык:
  +( (a(-a)* b(-b)*)* (a(-a)*)? | (b(-b)* a(-a)*)* (b(-b)*)? )?+

ДИАГРАММА ПЕРЕХОДОВ КА (ДКА)

Состояния:
  q0  — начальное состояние (ничего не прочитано)
  q1  — прочитан открывающий '+'
  q2  — последний значимый символ внутри — 'a'
  q3  — последний значимый символ внутри — 'b'
  q4a — прочитан '-' после 'a' (ожидается только 'a')
  q4b — прочитан '-' после 'b' (ожидается только 'b')
  q5  — принимающее состояние (прочитан закрывающий '+')
  qE  — состояние ошибки (тупиковое)

Таблица переходов:
         |  +   |  -   |  a   |  b
  -------|------|------|------|------
   q0    |  q1  |  qE  |  qE  |  qE
   q1    |  q5  |  qE  |  q2  |  q3
   q2    |  q5  |  q4a |  qE  |  q3
   q3    |  q5  |  q4b |  q2  |  qE
   q4a   |  qE  |  qE  |  q2  |  qE
   q4b   |  qE  |  qE  |  qE  |  q3
   q5    |  qE  |  qE  |  qE  |  qE   (принимающее, нет переходов)
   qE    |  qE  |  qE  |  qE  |  qE   (тупиковое)

Принимающее состояние: {q5}
"""

# ---------------------------------------------------------------
# Общие константы КА
# ---------------------------------------------------------------

# Псевдонимы типов для читаемости
State = str  # тип состояния КА
Symbol = str  # тип символа алфавита
TransitionTable = dict[State, dict[Symbol, State]]  # тип таблицы переходов

# Алфавит языка — множество допустимых символов
ALPHABET: set[Symbol] = {"+", "-", "a", "b"}

# Состояния КА (именованные константы)
Q0: State = "q0"  # начальное состояние
Q1: State = "q1"  # прочитан открывающий '+'
Q2: State = "q2"  # последний внутренний символ — 'a'
Q3: State = "q3"  # последний внутренний символ — 'b'
Q4A: State = "q4a"  # прочитан '-' после 'a' (ожидается только 'a')
Q4B: State = "q4b"  # прочитан '-' после 'b' (ожидается только 'b')
Q5: State = "q5"  # принимающее (прочитан закрывающий '+')
QE: State = "qE"  # состояние ошибки (тупиковое)

# Таблица переходов ДКА: TRANSITION[текущее_состояние][символ] -> следующее_состояние
TRANSITION: TransitionTable = {
    Q0: {"+": Q1, "-": QE, "a": QE, "b": QE},  # из начального — только '+' допустим
    Q1: {"+": Q5, "-": QE, "a": Q2, "b": Q3},  # после '+'  — пустая цепочка или a/b
    Q2: {
        "+": Q5,
        "-": Q4A,
        "a": QE,
        "b": Q3,
    },  # после 'a'  — '-' ведёт в q4a (ожидаем a)
    Q3: {
        "+": Q5,
        "-": Q4B,
        "a": Q2,
        "b": QE,
    },  # после 'b'  — '-' ведёт в q4b (ожидаем b)
    Q4A: {"+": QE, "-": QE, "a": Q2, "b": QE},  # после '-' (из a) — только 'a' допустим
    Q4B: {"+": QE, "-": QE, "a": QE, "b": Q3},  # после '-' (из b) — только 'b' допустим
    Q5: {"+": QE, "-": QE, "a": QE, "b": QE},  # принимающее — переходов нет
    QE: {"+": QE, "-": QE, "a": QE, "b": QE},  # тупиковое   — поглощает всё
}

# Принимающее состояние автомата
ACCEPT_STATE: State = Q5


# ---------------------------------------------------------------
# Часть 1: Подпрограмма — распознающий конечный автомат (КА)
# ---------------------------------------------------------------


def dfa_recognize(chain: str) -> bool:
    """
    Подпрограмма-распознаватель: проверяет, принадлежит ли
    цепочка chain языку с помощью детерминированного КА.

    Алгоритм:
      1. Начинаем в состоянии q0.
      2. Для каждого символа цепочки выполняем переход по таблице TRANSITION.
      3. Если символ не из алфавита или автомат попал в qE — возвращаем False.
      4. После прочтения всей цепочки возвращаем True только если
         текущее состояние — принимающее (q5).

    Параметры:
        chain: str — строка символов из алфавита {+, -, a, b}

    Возвращает:
        bool — True если цепочка принадлежит языку, False иначе
    """
    # Начинаем в начальном состоянии
    state: State = Q0

    for symbol in chain:
        # Символ не из алфавита — цепочка не принадлежит языку
        if symbol not in ALPHABET:
            return False

        # Выполняем переход по таблице переходов
        state = TRANSITION[state][symbol]

        # Тупиковое состояние — дальнейшее чтение бессмысленно
        if state == QE:
            return False

    # Цепочка принята, если автомат завершил в принимающем состоянии
    return state == ACCEPT_STATE


# ---------------------------------------------------------------
# Часть 2: Программа поиска цепочек языка в последовательности
# ---------------------------------------------------------------


def find_chains(sequence: str) -> list[tuple[int, str]]:
    """
    Ищет все цепочки языка в последовательности символов.
    Использует подпрограмму dfa_recognize (часть 1) для проверки
    каждого кандидата на принадлежность языку.

    Алгоритм (жадный поиск с перекрытием):
      Для каждой позиции i (начало потенциальной цепочки):
        - Если sequence[i] != '+', пропускаем (цепочка начинается с '+').
        - Иначе перебираем все возможные концы j от i+1 до n,
          ограничиваясь символами алфавита.
          Для каждой подстроки sequence[i..j] вызываем dfa_recognize.
          Запоминаем конец самой длинной принятой цепочки (жадность).
        - После нахождения самой длинной цепочки с позиции i:
            * Следующий поиск начинается с last_accept_end - 1,
              т.к. закрывающий '+' может быть открывающим следующей цепочки.
            * Если цепочка не найдена — переходим к i+1.

    Параметры:
        sequence: str — входная последовательность символов

    Возвращает:
        list[tuple[int, str]] — список пар (позиция, цепочка),
        где позиция — 1-based индекс начала цепочки в последовательности
    """
    # Список найденных цепочек: (1-based позиция, цепочка)
    results: list[tuple[int, str]] = []
    # Длина входной последовательности
    n: int = len(sequence)
    # Текущая позиция начала поиска (0-based)
    i: int = 0

    while i < n:
        # Цепочка языка всегда начинается с '+' — пропускаем остальные символы
        if sequence[i] != "+":
            i += 1
            continue

        # Конец самой длинной принятой цепочки с позиции i (exclusive, -1 = не найдено)
        last_accept_end: int = -1

        # Перебираем все возможные концы кандидата sequence[i..j]
        for j in range(i + 1, n + 1):
            # Если очередной символ не из алфавита — дальше кандидатов нет
            if sequence[j - 1] not in ALPHABET:
                break

            # Формируем кандидата и проверяем через подпрограмму-распознаватель КА
            candidate: str = sequence[i:j]
            if dfa_recognize(candidate):
                # Кандидат принят — запоминаем как текущий лучший конец
                last_accept_end = j

        if last_accept_end != -1:
            # Извлекаем найденную (самую длинную) цепочку
            chain: str = sequence[i:last_accept_end]

            # Добавляем в результат с 1-based позицией
            results.append((i + 1, chain))

            # закрывающий '+' считается началом следующей возможной
            # цепочки, поэтому следующий поиск начинается с last_accept_end - 1
            i = last_accept_end - 1
        else:
            # Цепочка не найдена с позиции i — переходим к следующей
            i += 1

    return results


# ---------------------------------------------------------------
# Главная программа
# ---------------------------------------------------------------


def main() -> None:
    """
    Главная программа:
      - Читает входную последовательность символов из stdin
      - Ищет все цепочки языка с помощью find_chains
      - Выводит результат в формате: номер: цепочка
      - Если цепочек нет — выводит 'цепочек не найдено'
    """
    print("Введите последовательность символов алфавита {+, -, a, b}:")
    sequence: str = input().strip()

    # Поиск всех цепочек языка в последовательности
    chains: list[tuple[int, str]] = find_chains(sequence)

    if not chains:
        print("цепочек не найдено")
    else:
        # Вывод в формате: номер: цепочка
        for pos, chain in chains:
            print(f"{pos}: {chain}")


if __name__ == "__main__":
    main()
