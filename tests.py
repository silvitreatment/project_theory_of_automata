# ---------------------------------------------------------------
# Тесты подпрограммы-распознавателя КА
# ---------------------------------------------------------------


class TestDfaRecognize:
    """Тесты подпрограммы dfa_recognize."""

    @pytest.mark.parametrize(
        "chain",
        [
            "++",  # пустая внутренняя последовательность
            "+a+",  # одна буква a
            "+b+",  # одна буква b
            "+ab+",  # разные буквы без разделителя
            "+ba+",  # разные буквы без разделителя (обратный порядок)
            "+a-a+",  # одинаковые a с разделителем
            "+b-b+",  # одинаковые b с разделителем
            "+abab+",  # чередование разных букв
            "+a-a-ab-b-bab-b+",  # длинная цепочка из задания
            "+aba-ab+",  # смешанная последовательность
            "+bab-ba+",  # смешанная последовательность
            "+a-aba-a+",  # серия a, b, серия a
        ],
    )
    def test_valid(self, chain: str) -> None:
        """Допустимые цепочки языка должны распознаваться как True."""
        assert (
            dfa_recognize(chain) is True
        ), f"Цепочка {chain!r} должна принадлежать языку"

    @pytest.mark.parametrize(
        "chain",
        [
            "+aa+",  # два одинаковых a без разделителя
            "+bb+",  # два одинаковых b без разделителя
            "+a-+",  # разделитель перед закрывающим '+'
            "+-a+",  # разделитель сразу после открывающего '+'
            "+a--a+",  # двойной разделитель
            "+a--b+",  # двойной разделитель перед другим символом
            "+a-b+",  # '-' между РАЗНЫМИ символами — недопустимо!
            "+b-a+",  # '-' между РАЗНЫМИ символами — недопустимо!
            "+a-b-a+",  # '-' между разными символами — недопустимо!
            "+a-a-b+",  # '-' между a и b (разными) — недопустимо!
            "+b-b-a+",  # '-' между b и a (разными) — недопустимо!
            "a+",  # нет открывающего '+'
            "+a",  # нет закрывающего '+'
            "",  # пустая строка
            "+",  # только один '+'
            "+-+",  # только разделитель внутри
            "+c+",  # символ вне алфавита
            "+aab+",  # aa без разделителя в середине
            "+abb+",  # bb без разделителя в конце
        ],
    )
    def test_invalid(self, chain: str) -> None:
        """Недопустимые цепочки должны распознаваться как False."""
        assert (
            dfa_recognize(chain) is False
        ), f"Цепочка {chain!r} не должна принадлежать языку"


